    private static FrameScopeToggleCheckBox ToggleCheck(string text, bool isChecked)
    {
        return new FrameScopeToggleCheckBox
        {
            Text = text,
            Checked = isChecked,
            Dock = DockStyle.Fill,
            BackColor = Color.Transparent,
            Accent = UiCyan,
            TextColor = UiText,
            MutedColor = UiMuted
        };
    }

    private static Control SettingsToggleButton(string text, CheckBox backing)
    {
        var check = ToggleCheck(text, backing != null && backing.Checked);
        check.Margin = new Padding(0, 5, 0, 5);
        check.BackColor = Color.Transparent;
        check.CheckedChanged += delegate
        {
            if (backing != null && backing.Checked != check.Checked)
            {
                backing.Checked = check.Checked;
            }
        };
        if (backing != null)
        {
            backing.CheckedChanged += delegate
            {
                if (check.Checked != backing.Checked)
                {
                    check.Checked = backing.Checked;
                }
            };
        }
        return check;
    }

