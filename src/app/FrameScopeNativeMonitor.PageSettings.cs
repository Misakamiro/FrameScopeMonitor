        return box;
    }

    private static Control RoundedInputHost(string text, out TextBox box)
    {
        var textBox = new TextBox
        {
            Text = text,
            Dock = DockStyle.Fill,
            BackColor = Color.FromArgb(12, 40, 64),
            ForeColor = UiText,
            BorderStyle = BorderStyle.FixedSingle,
            Font = new Font("Microsoft YaHei UI", 9.4f),
            Margin = new Padding(0, 7, 0, 7)
        };
        var tooltip = new ToolTip { AutomaticDelay = 120, AutoPopDelay = 12000 };
        textBox.HandleCreated += delegate
        {
            if (textBox.TextLength > 0)
            {
                textBox.SelectionStart = textBox.TextLength;
                textBox.ScrollToCaret();
            }
        };
        tooltip.SetToolTip(textBox, text ?? "");
        MakeRounded(textBox, UiRadiusControl);
        box = textBox;
        return textBox;
    }
