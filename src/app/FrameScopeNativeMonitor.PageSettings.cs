    private static TextBox DarkTextBox(string text)
    {
        var box = new TextBox
        {
            Text = text,
            Dock = DockStyle.Fill,
            BackColor = Color.FromArgb(12, 40, 64),
            ForeColor = UiText,
            BorderStyle = BorderStyle.None,
