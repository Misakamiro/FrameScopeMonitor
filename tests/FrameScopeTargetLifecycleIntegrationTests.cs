            WaitUntil(delegate
            {
                return CountValidCsvRows(Path.Combine(runDir, "process-samples.csv"), 11) > processRowsAfterHandoff &&
                    CountValidCsvRows(Path.Combine(runDir, "system-samples.csv"), 21) > systemRowsAfterHandoff;
            }, 6000, "sampler CSV rows continue increasing while replacement alias lives");
            AssertTrue(IsProcessRunning(monitorPid), "same monitor remains active for replacement alias");

            File.WriteAllText(bStop, "stop");
            AssertTrue(targetB.WaitForExit(5000), "replacement alias exits");
            DateTime replacementExitedAt = DateTime.Now;
            Thread.Sleep(1000);
            AssertTrue(IsProcessRunning(monitorPid), "monitor honors the bounded quiescence window");

            Stopwatch finalExit = Stopwatch.StartNew();
            WaitUntil(delegate { return !IsProcessRunning(monitorPid); }, 8000, "monitor exits after replacement alias quiescence");
            finalExit.Stop();
            AssertTrue(finalExit.ElapsedMilliseconds >= 700, "monitor does not skip remaining quiescence");
            AssertTrue(finalExit.ElapsedMilliseconds <= 8000, "monitor true-exit delay remains bounded");

            AssertTrue(watcher.WaitForExit(30000), "watcher exits after report completion");
            AssertEqual(0, watcher.ExitCode, "watcher exit code");
            AssertEqual(1, FindRunDirectories(dataRoot).Length, "one monitor run across alias handoff");

