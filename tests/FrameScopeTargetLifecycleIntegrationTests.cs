    private static void RunOverlappingWatcherClaimScenario()
    {
        const string fooBaseName = "FrameScopeOverlapFoo";
        const string barBaseName = "FrameScopeOverlapBar";
        AssertNoExistingProcess(fooBaseName);
        AssertNoExistingProcess(barBaseName);

        string testRoot = AppDomain.CurrentDomain.BaseDirectory;
        string root = Path.GetFullPath(Path.Combine(testRoot, ".."));
        string fixture = Path.Combine(Path.GetTempPath(), "framescope-overlap-claim-" + Guid.NewGuid().ToString("N"));
        retainedFixture = fixture;
        string dataRoot = Path.Combine(fixture, "runs");
        string toolsDir = Path.Combine(fixture, "tools");
        Directory.CreateDirectory(dataRoot);
        Directory.CreateDirectory(toolsDir);

        CopyRuntime(root, fixture);
        string testExe = Path.Combine(testRoot, "FrameScopeTargetLifecycleIntegrationTests.exe");
        string fooExe = Path.Combine(fixture, fooBaseName + ".exe");
        File.Copy(testExe, fooExe, true);
        File.Copy(Path.Combine(testRoot, "FrameScopePubgFakePresentMon.exe"), Path.Combine(toolsDir, "PresentMon-2.4.1-x64.exe"), true);

        string configPath = Path.Combine(fixture, "framescope-overlap-config.json");
        FrameScopeConfigStore.Save(configPath, new FrameScopeConfig
        {
            PollIntervalMs = FrameScopeConfigStore.InternalPollIntervalMs,
            TelemetrySampleIntervalMs = 500,
            DataRoot = dataRoot,
            OpenReportOnComplete = false,
            AutoGenerateDiagnosticReport = false,
            MonitorScript = FrameScopeConfigStore.NativeMonitorMode,
            CpuTelemetry = new FrameScopeCpuTelemetryConfig
            {
                CollectPerCoreFrequency = false,
                CollectCpuVoltage = false,
                VoltageProvider = "disabled"
            },
            Targets = new List<FrameScopeTarget>
            {
                new FrameScopeTarget
                {
                    Enabled = true,
                    Name = "Overlap Primary",
                    ProcessName = fooBaseName + ".exe",
                    OpenReportOnComplete = false
                },
                new FrameScopeTarget
                {
                    Enabled = true,
                    Name = "Overlap Expanded",
                    ProcessName = fooBaseName + ".exe;" + barBaseName + ".exe",
                    OpenReportOnComplete = false
                }
            }
        });

        string fooReady = Path.Combine(fixture, "foo.ready");
        string fooStop = Path.Combine(fixture, "foo.stop");
        string statePath = Path.Combine(fixture, "framescope-watcher-state.json");
        Process foo = null;
        Process watcher = null;
        HashSet<int> monitorPids = new HashSet<int>();
        bool passed = false;

        try
        {
            foo = StartHidden(fooExe, JoinArguments("--helper-ready", fooReady, "--helper-stop", fooStop), fixture);
            WaitUntil(delegate { return File.Exists(fooReady); }, 5000, "overlap helper ready");
            watcher = StartHidden(
                Path.Combine(fixture, "FrameScopeMonitor.exe"),
                JoinArguments("--watcher", "--config", configPath, "--exit-after-first-run"),
                fixture);

            string expectedKey = FrameScopeTargetLifecycle.CanonicalBaseNameTargetKey(new[] { fooBaseName });
            string expectedAlias = fooBaseName.ToLowerInvariant();
            List<Dictionary<string, object>> active = null;
            int monitorPid = 0;
            WaitUntil(delegate
            {
                List<Dictionary<string, object>> observed = TryReadActiveMonitors(statePath);
                if (observed == null) return false;

                foreach (Dictionary<string, object> monitor in observed)
                {
                    int observedPid;
                    if (TryReadMonitorPid(monitor, out observedPid)) monitorPids.Add(observedPid);
                }

                if (observed.Count != 1) return false;
                Dictionary<string, object> candidate = observed[0];
                object keyValue;
                object aliasesValue;
                int candidatePid;
                if (!candidate.TryGetValue("Key", out keyValue) ||
                    !string.Equals(expectedKey, Convert.ToString(keyValue, CultureInfo.InvariantCulture), StringComparison.Ordinal) ||
                    !candidate.TryGetValue("Aliases", out aliasesValue) ||
                    !TryReadMonitorPid(candidate, out candidatePid))
                {
                    return false;
                }

                List<string> candidateAliases = ReadStringList(aliasesValue);
                if (candidateAliases.Count != 1 ||
                    !string.Equals(expectedAlias, candidateAliases[0], StringComparison.Ordinal))
                {
                    return false;
                }

                active = observed;
                monitorPid = candidatePid;
                return true;
            }, 15000, "overlap watcher matching active state");

            AssertTrue(active != null, "overlap watcher state parses");
            AssertEqual(1, active.Count, "overlapping targets claim exactly one active monitor");
            Dictionary<string, object> claimed = active[0];
            AssertEqual(expectedKey, Convert.ToString(claimed["Key"], CultureInfo.InvariantCulture), "overlap state canonical key");
            List<string> aliases = ReadStringList(claimed["Aliases"]);
            AssertEqual(1, aliases.Count, "overlap state claimed alias count");
            AssertEqual(expectedAlias, aliases[0], "overlap state normalized alias");

            AssertTrue(IsProcessRunning(monitorPid), "overlap claimed monitor is running");

            TrySignal(fooStop);
            AssertTrue(foo.WaitForExit(5000), "overlap helper exits");
            AssertTrue(watcher.WaitForExit(30000), "overlap watcher exits after one completed run");
            AssertEqual(0, watcher.ExitCode, "overlap watcher exit code");
            AssertEqual(1, FindRunDirectories(dataRoot).Length, "overlapping targets create one run");

            passed = true;
            retainedFixture = "";
        }
        finally
        {
            TrySignal(fooStop);
            TryStop(foo);
            TryStop(watcher);
            foreach (int monitorPid in monitorPids) TryStop(monitorPid);
            foreach (string runDir in FindRunDirectories(dataRoot)) TryStopPresentMon(runDir);
            if (passed) TryDeleteDirectory(fixture);
        }
    }

