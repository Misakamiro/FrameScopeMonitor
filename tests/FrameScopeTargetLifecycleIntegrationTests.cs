            Directory.CreateDirectory(decoyRunDir);
            Directory.SetLastWriteTimeUtc(decoyRunDir, DateTime.UtcNow.AddMinutes(10));

            File.WriteAllText(aStop, "stop");
            AssertTrue(targetA.WaitForExit(5000), "primary alias exits");
            DateTime primaryExitedAt = DateTime.Now;

            Thread.Sleep(600);
            AssertTrue(IsProcessRunning(monitorPid), "monitor survives the A-to-B alias gap");
            AssertTrue(!watcher.HasExited, "watcher remains active during alias gap");

            targetB = StartHidden(targetBExe, JoinArguments("--helper-ready", bReady, "--helper-stop", bStop), fixture);
            WaitUntil(delegate { return File.Exists(bReady); }, 5000, "replacement alias ready");
            WaitUntil(delegate { return CsvContains(Path.Combine(runDir, "process-samples.csv"), "TslGame-Win64-Shipping"); }, 7000, "process CSV records replacement alias");

