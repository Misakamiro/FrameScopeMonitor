    private static void CopyRuntime(string root, string fixture)
    {
        string[] executables =
        {
            "FrameScopeMonitor.exe",
            "FrameScopeProcessSampler.exe",
            "FrameScopeSystemSampler.exe",
            "FrameScopeReportGenerator.exe"
        };
        foreach (string name in executables)
        {
            string source = Path.Combine(root, name);
            if (!File.Exists(source)) throw new Exception("Production build output missing: " + source);
            File.Copy(source, Path.Combine(fixture, name), true);
        }
        foreach (string dll in Directory.GetFiles(root, "*.dll", SearchOption.TopDirectoryOnly))
        {
            File.Copy(dll, Path.Combine(fixture, Path.GetFileName(dll)), true);
        }
    }

    private static Process StartHidden(string fileName, string arguments, string workingDirectory)
    {
        Process process = Process.Start(new ProcessStartInfo
        {
            FileName = fileName,
            Arguments = arguments,
            WorkingDirectory = workingDirectory,
            UseShellExecute = false,
            CreateNoWindow = true,
            WindowStyle = ProcessWindowStyle.Hidden
        });
        if (process == null) throw new Exception("Failed to start: " + fileName);
        return process;
    }

    private static string JoinArguments(params string[] values)
    {
        return string.Join(" ", values.Select(Quote).ToArray());
    }

    private static string Quote(string value)
    {
        string text = value ?? "";
        return "\"" + text.Replace("\\", "\\\\").Replace("\"", "\\\"") + "\"";
    }

    private static void WaitUntil(Func<bool> condition, int timeoutMs, string label)
    {
        Stopwatch wait = Stopwatch.StartNew();
        Exception last = null;
        while (wait.ElapsedMilliseconds < timeoutMs)
        {
            try
            {
                if (condition()) return;
                last = null;
            }
            catch (Exception ex)
            {
                last = ex;
            }
            Thread.Sleep(50);
        }
        throw new Exception(label + " timed out after " + timeoutMs + " ms" + (last == null ? "" : ": " + last.Message));
    }

    private static int TryReadMonitorPid(string statePath)
    {
        try
        {
            string text = File.ReadAllText(statePath, Encoding.UTF8);
            Match match = Regex.Match(text, "\\\"MonitorPid\\\"\\s*:\\s*(\\d+)", RegexOptions.IgnoreCase);
            return match.Success ? Int32.Parse(match.Groups[1].Value, CultureInfo.InvariantCulture) : 0;
        }
        catch
        {
            return 0;
        }
    }

    private static List<Dictionary<string, object>> TryReadActiveMonitors(string statePath)
    {
        try
        {
            Dictionary<string, object> state = ReadJson(statePath);
            object value;
            if (!state.TryGetValue("ActiveMonitors", out value)) return null;
            var result = new List<Dictionary<string, object>>();
            IEnumerable items = value as IEnumerable;
            if (items == null) return result;
            foreach (object item in items)
            {
                Dictionary<string, object> monitor = item as Dictionary<string, object>;
                if (monitor != null) result.Add(monitor);
            }
            return result;
        }
        catch
        {
            return null;
        }
    }

    private static List<string> ReadStringList(object value)
    {
        var result = new List<string>();
        IEnumerable items = value as IEnumerable;
        if (items == null || value is string) return result;
        foreach (object item in items) result.Add(Convert.ToString(item, CultureInfo.InvariantCulture));
        return result;
    }

    private static bool TryReadMonitorPid(Dictionary<string, object> monitor, out int monitorPid)
    {
        monitorPid = 0;
        object value;
        if (monitor == null || !monitor.TryGetValue("MonitorPid", out value)) return false;
        return int.TryParse(
            Convert.ToString(value, CultureInfo.InvariantCulture),
            NumberStyles.Integer,
            CultureInfo.InvariantCulture,
            out monitorPid) && monitorPid > 0;
    }

    private static string FindRunDirectory(string dataRoot)
    {
        string[] runs = FindRunDirectories(dataRoot);
        return runs.Length == 0 ? "" : runs.OrderByDescending(Directory.GetLastWriteTimeUtc).First();
    }

    private static string[] FindRunDirectories(string dataRoot)
    {
        if (!Directory.Exists(dataRoot)) return new string[0];
        try
        {
            return Directory.GetFiles(dataRoot, "status.json", SearchOption.AllDirectories)
                .Select(Path.GetDirectoryName)
                .Where(path => !string.IsNullOrWhiteSpace(path))
                .Distinct(StringComparer.OrdinalIgnoreCase)
                .ToArray();
        }
        catch
        {
            return new string[0];
        }
    }

    private static int CountValidCsvRows(string path, int expectedColumns)
    {
        if (!File.Exists(path)) return 0;
        try
        {
            return ReadSharedLines(path)
                .Skip(1)
                .Count(line => !string.IsNullOrWhiteSpace(line) && line.Split(',').Length == expectedColumns);
        }
        catch
        {
            return 0;
        }
    }

    private static bool CsvContains(string path, string value)
    {
        try { return File.Exists(path) && string.Join("\n", ReadSharedLines(path)).IndexOf(value, StringComparison.OrdinalIgnoreCase) >= 0; }
        catch { return false; }
    }

    private static void AssertContinuousSampleIndexes(string path, int column, string label)
    {
        List<int> indexes = new List<int>();
        foreach (string line in ReadSharedLines(path).Skip(1))
        {
            string[] cells = line.Split(',');
            int value;
            if (cells.Length > column && Int32.TryParse(cells[column], NumberStyles.Integer, CultureInfo.InvariantCulture, out value)) indexes.Add(value);
        }
        int[] distinct = indexes.Distinct().OrderBy(value => value).ToArray();
        AssertTrue(distinct.Length >= 3, label + " has multiple samples");
        for (int i = 0; i < distinct.Length; i++) AssertEqual(i, distinct[i], label + " item " + i);
    }

    private static DateTime LatestCsvTimestamp(string path)
    {
        DateTime latest = DateTime.MinValue;
        foreach (string line in ReadSharedLines(path).Skip(1))
        {
            string[] cells = line.Split(',');
            DateTime value;
            if (cells.Length > 0 && DateTime.TryParse(cells[0], CultureInfo.InvariantCulture, DateTimeStyles.RoundtripKind, out value) && value > latest) latest = value;
        }
        return latest;
    }

    private static string[] ReadSharedLines(string path)
    {
        var lines = new List<string>();
        using (var stream = new FileStream(path, FileMode.Open, FileAccess.Read, FileShare.ReadWrite | FileShare.Delete))
        using (var reader = new StreamReader(stream, Encoding.UTF8, true))
        {
            string line;
            while ((line = reader.ReadLine()) != null) lines.Add(line);
        }
        return lines.ToArray();
    }

    private static Dictionary<string, object> ReadJson(string path)
    {
        return Json.Deserialize<Dictionary<string, object>>(File.ReadAllText(path, Encoding.UTF8));
    }

