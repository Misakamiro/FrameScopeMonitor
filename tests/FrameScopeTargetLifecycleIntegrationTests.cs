    private static bool IsProcessRunning(int pid)
    {
        try
        {
            using (Process process = Process.GetProcessById(pid)) return !process.HasExited;
        }
        catch
        {
            return false;
        }
    }

    private static void AssertNoExistingProcess(string name)
    {
        Process[] processes = Process.GetProcessesByName(name);
        try { AssertEqual(0, processes.Length, "no pre-existing " + name + " process"); }
        finally { foreach (Process process in processes) process.Dispose(); }
    }

    private static void TryStopPresentMon(string runDir)
    {
        if (string.IsNullOrWhiteSpace(runDir)) return;
        try
        {
            Dictionary<string, object> status = ReadJson(Path.Combine(runDir, "status.json"));
            object value;
            if (status.TryGetValue("PresentMonSessionName", out value)) TrySignal(FrameScopePubgSimulationCommon.StopSentinelPath(Convert.ToString(value, CultureInfo.InvariantCulture)));
        }
        catch { }
    }

    private static void TrySignal(string path)
    {
        if (string.IsNullOrWhiteSpace(path)) return;
        try { File.WriteAllText(path, "stop"); }
        catch { }
    }

    private static void TryStop(Process process)
    {
        if (process == null) return;
        try { if (!process.HasExited) process.Kill(); }
        catch { }
        try { process.WaitForExit(3000); }
        catch { }
        try { process.Dispose(); }
        catch { }
    }

    private static void TryStop(int pid)
    {
        try { using (Process process = Process.GetProcessById(pid)) TryStop(process); }
        catch { }
    }

