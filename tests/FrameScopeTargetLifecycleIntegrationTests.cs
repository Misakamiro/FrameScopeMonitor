    public static int Main(string[] args)
    {
        string helperReady = FrameScopePubgSimulationCommon.GetArgValue(args, "--helper-ready", "");
        if (!string.IsNullOrWhiteSpace(helperReady))
        {
            return RunTargetHelper(helperReady, FrameScopePubgSimulationCommon.GetArgValue(args, "--helper-stop", ""));
        }
        if (FrameScopePubgSimulationCommon.HasFlag(args, "--system-csv") &&
            string.Equals(Environment.GetEnvironmentVariable("FRAMESCOPE_INTEGRATION_FAIL_SYSTEM_SAMPLER"), "1", StringComparison.Ordinal))
        {
            Console.Error.WriteLine("synthetic system sampler exited before owner stop");
            return 7;
        }

        bool createdNew;
        using (var mutex = new Mutex(true, "Local\\FrameScopeTargetLifecycleIntegrationTests", out createdNew))
        {
            if (!createdNew)
            {
                Console.Error.WriteLine("FrameScopeTargetLifecycleIntegrationTests is already running.");
                return 1;
            }

            try
            {
                RunAliasHandoffScenario();
                RunFailedSystemSamplerWatcherScenario();
                RunOverlappingWatcherClaimScenario();
                Console.WriteLine("FrameScopeTargetLifecycleIntegrationTests: PASS");
                return 0;
            }
            catch (Exception ex)
            {
                Console.Error.WriteLine(ex.GetType().FullName + ": " + ex.Message);
                Console.Error.WriteLine(ex.StackTrace ?? "");
                if (!string.IsNullOrWhiteSpace(retainedFixture)) Console.Error.WriteLine("Fixture retained: " + retainedFixture);
                return 1;
            }
            finally
            {
                try { mutex.ReleaseMutex(); }
                catch { }
            }
        }
    }

    private static int RunTargetHelper(string readyPath, string stopPath)
    {
        try
        {
            EnsureParent(readyPath);
            File.WriteAllText(readyPath, DateTime.Now.ToString("o", CultureInfo.InvariantCulture));
            Stopwatch timeout = Stopwatch.StartNew();
            while (timeout.ElapsedMilliseconds < 30000 && !File.Exists(stopPath)) Thread.Sleep(25);
            return File.Exists(stopPath) ? 0 : 2;
        }
        catch
        {
            return 1;
        }
    }

